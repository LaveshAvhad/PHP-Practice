$num = 50;
switch ($num) {
    case 1:
        echo "$num is equal to 50";
        break;
    case 2:
        echo "$num is equal to 40";
        break;
    case 3:
        echo "$num is equal to 30";
        break;
    case 4:
        echo "$num is equal to 20";
        break;

    default:
        echo "Invalid Number";
}
